#include "tetriminos.h"

void	ft_resetx(t_tetriminos t)
{
	int i;
	int x;

	x = t[0].x;
	i = 1;
	while (i < 4)
	{
		if (t[i].x < x)
			x = t[i].x;
		i++;
	}
	i = 0;
	while (i < 4)
	{
		t[i].x = t[i].x - x;
		i++;
	}
}
