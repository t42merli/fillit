#include "tetriminos.h"

int	ft_reset(t_tetriminos t)
{
	int i;
	int y;

	i = 1;
	y = t[0].y;
	ft_resetx(t);
	while (i < 4)
	{
		if (t[i].y < y)
			y = t[i].y;
		i++;
	}
	i = 0;
	while (i < 4)
	{
		t[i].y = t[i].y - y;
		i++;
	}
	return (0);
}
